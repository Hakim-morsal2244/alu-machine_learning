#!/usr/bin/env python3
"""
Module that contains a function to concatenate two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenate two lists and return a new list.

    Args:
        arr1 (list): First list of ints/floats.
        arr2 (list): Second list of ints/floats.

    Returns:
        list: New concatenated list.
    """
    return arr1 + arr2
